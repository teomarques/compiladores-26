class ExprArith {
    public static void main(String[] args) {
        int a;
        int b;
        a = 6;
        b = 2;
        System.out.print(a + b + 3);
        System.out.print("\n");
        System.out.print(a - b);
        System.out.print("\n");
        System.out.print(a * b);
        System.out.print("\n");
        System.out.print(a / b);
        System.out.print("\n");
        System.out.print(a % b);
        System.out.print("\n");
    }
}
