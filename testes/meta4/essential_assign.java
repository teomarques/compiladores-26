class EssentialAssign {
    public static void main(String[] args) {
        int a;
        double d;
        boolean b;
        a = 10;
        d = 5.0;
        b = true;
        System.out.print(a);
        System.out.print("\n");
        System.out.print(d);
        System.out.print("\n");
        System.out.print(b);
        System.out.print("\n");
    }
}
