class m4_essential_unary {
	public static void main(String[] args) {
		int i;
		double d;
		i = 5;
		System.out.print(+i);
		System.out.print("\n");
		System.out.print(-i);
		System.out.print("\n");
		d = 3.5;
		System.out.print(+d);
		System.out.print("\n");
		System.out.print(-d);
		System.out.print("\n");
	}
}
