class m4_ctrl_if {
	public static void main(String[] args) {
		int x;
		x = 7;
		if (x < 10) {
			System.out.print(1);
			System.out.print("\n");
		} else {
			System.out.print(2);
			System.out.print("\n");
		}
		x = 20;
		if (x < 10) {
			System.out.print(3);
			System.out.print("\n");
		} else {
			System.out.print(4);
			System.out.print("\n");
		}
	}
}
