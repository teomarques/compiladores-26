class ExprRel {
    public static void main(String[] args) {
        int a;
        int b;
        a = 3;
        b = 5;
        System.out.print(a < b);
        System.out.print("\n");
        System.out.print(a > b);
        System.out.print("\n");
        System.out.print(a == b);
        System.out.print("\n");
        System.out.print(a != b);
        System.out.print("\n");
        System.out.print(a <= b);
        System.out.print("\n");
        System.out.print(a >= b);
        System.out.print("\n");
    }
}
