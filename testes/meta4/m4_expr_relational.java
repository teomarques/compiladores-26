class m4_expr_relational {
	public static void main(String[] args) {
		System.out.print(3 < 5);
		System.out.print("\n");
		System.out.print(5 > 4);
		System.out.print("\n");
		System.out.print(3 <= 3);
		System.out.print("\n");
		System.out.print(4 >= 5);
		System.out.print("\n");
		System.out.print(2 == 2);
		System.out.print("\n");
		System.out.print(2 != 3);
		System.out.print("\n");
	}
}
