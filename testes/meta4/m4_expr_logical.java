class m4_expr_logical {
	public static void main(String[] args) {
		boolean t;
		boolean f;
		t = true;
		f = false;
		System.out.print(t && f);
		System.out.print("\n");
		System.out.print(t || f);
		System.out.print("\n");
		System.out.print(t ^ f);
		System.out.print("\n");
		System.out.print(!f);
		System.out.print("\n");
	}
}
