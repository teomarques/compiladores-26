class MethodsReturn {
    public static int iret() {
        return 4;
    }

    public static double dret() {
        return 2.5;
    }

    public static boolean bret() {
        return true;
    }

    public static void main(String[] args) {
        System.out.print(iret());
        System.out.print("\n");
        System.out.print(dret());
        System.out.print("\n");
        System.out.print(bret());
        System.out.print("\n");
    }
}
