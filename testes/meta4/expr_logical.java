class ExprLogical {
    public static void main(String[] args) {
        boolean a;
        boolean b;
        a = true;
        b = false;
        System.out.print(a && b);
        System.out.print("\n");
        System.out.print(a || b);
        System.out.print("\n");
        System.out.print(!a);
        System.out.print("\n");
    }
}
