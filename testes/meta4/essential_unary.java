class EssentialUnary {
    public static void main(String[] args) {
        int x;
        boolean b;
        x = 5;
        b = true;
        System.out.print(-x);
        System.out.print("\n");
        System.out.print(+-x);
        System.out.print("\n");
        System.out.print(!b);
        System.out.print("\n");
    }
}
