class m4_essential_fields {
	public static int counter;
	public static double ratio;
	public static void main(String[] args) {
		counter = 7;
		System.out.print(counter);
		System.out.print("\n");
		counter = counter + 3;
		System.out.print(counter);
		System.out.print("\n");
		ratio = 2.5;
		System.out.print(ratio);
		System.out.print("\n");
	}
}
